    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        String gt = rdoNam.isSelected() ? "Nam" : "Nữ";
        try {
            SinhVien sv = new SinhVien(txtMa.getText(), txtTen.getText(), txtEmail.getText(),
                    txtSDT.getText(), gt, txtDiaChi.getText());
            list.add(sv);
            if (this.them(sv)) {
                JOptionPane.showMessageDialog(this, "Thêm thành công!!!");
                this.loadDataToArray();
            } else {
                JOptionPane.showMessageDialog(this, "Thêm vào databse ko thành công!!!");
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnAddActionPerformed

