    public void showw(int i) {
        SinhVien sv = list.get(i);
        txtDiaChi.setText(sv.getDiaChi());
        txtMa.setText(sv.getMaSV());
        txtTen.setText(sv.getTenSV());
        txtEmail.setText(sv.getEmail());
        txtSDT.setText(sv.getSoDT());
        String gt = sv.getGioiTinh();
        if (gt.equalsIgnoreCase("Nam")) {
            rdoNam.setSelected(true);
        } else {
            rdoNu.setSelected(true);
        }

    }

    public boolean loadDataToArray() throws Exception {
        String sql = "SELECT * FROM dbo.STUDENT";
        try ( Connection conn = DatabaseHelper.openConnection();  PreparedStatement pstmt = conn.prepareStatement(sql)) {
            try ( ResultSet rs = pstmt.executeQuery()) {
                while (rs.next()) {
                    String masv = rs.getString(1);
                    String tensv = rs.getString(2);
                    String email = rs.getString(3);
                    String sdt = rs.getString(4);
                    String gioitinh = rs.getString(5);
                    String diachi = rs.getString(6);
                    SinhVien sv = new SinhVien(masv, tensv, email, sdt, gioitinh, diachi);
                    list.add(sv);
                }
            }
            conn.close();;
            return pstmt.executeUpdate() > 0;
        }
    }

    public boolean them(SinhVien sv) throws Exception {
        String sql = "INSERT dbo.STUDENT([MaSV], [TenSV], [Email], [SoDT], [GioiTinh], [DiaChi]) VALUES(?,?,?,?,?,?)";
        try ( Connection conn = DatabaseHelper.openConnection();  PreparedStatement pstmt = conn.prepareStatement(sql)) {
            pstmt.setString(1, sv.getMaSV());
            pstmt.setString(2, sv.getTenSV());
            pstmt.setString(3, sv.getEmail());
            pstmt.setString(4, sv.getSoDT());
            pstmt.setString(5, sv.getGioiTinh());
            pstmt.setString(6, sv.getDiaChi());

            return pstmt.executeUpdate() > 0;
        }
    }

    public boolean Xoa(String maSV) throws Exception {
        String sql = "DELETE FROM dbo.STUDENT WHERE [MaSV] = ?";
        try ( Connection conn = DatabaseHelper.openConnection();  PreparedStatement pstmt = conn.prepareStatement(sql)) {
            pstmt.setString(1, maSV);
            return pstmt.executeUpdate() > 0;
        }
    }

    public boolean update(SinhVien sv) throws Exception {
        String sql = "UPDATE dbo.STUDENT SET [TenSV] = ?, [Email] = ?, [SoDT] = ?, [GioiTinh] = ?, [DiaChi] = ? WHERE [MaSV] = ?";
        try ( Connection conn = DatabaseHelper.openConnection();  PreparedStatement pstmt = conn.prepareStatement(sql)) {
            pstmt.setString(6, sv.getMaSV());
            pstmt.setString(1, sv.getTenSV());
            pstmt.setString(2, sv.getEmail());
            pstmt.setString(3, sv.getSoDT());
            pstmt.setString(4, sv.getGioiTinh());
            pstmt.setString(5, sv.getDiaChi());

            return pstmt.executeUpdate() > 0;
        }
    }
    
