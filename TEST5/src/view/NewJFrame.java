    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:

        if (txtMa.getText().isEmpty() || txtSDT.getText().isEmpty() || txtTen.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "ko duoc de trong!!!!");
            return;
        }
        String sql = "insert into STUDENT(Masv,Hoten,IDClass,gioitinh,dienthoai) VALUES (?,?,?,?,?)";
        try {
            Connection conn = jdbcUtill.ketNoi();
            PreparedStatement pstmt = conn.prepareStatement(sql);
            pstmt.setString(1, txtMa.getText());
            pstmt.setString(2, txtTen.getText());
            pstmt.setString(3, cboLop.getSelectedItem().toString());
            String gender = rdoNAm.isSelected() ? "Nam" : "Nữ";
            pstmt.setString(4, gender);
            pstmt.setString(5, txtSDT.getText());

            pstmt.executeUpdate();
            conn.close();
            JOptionPane.showMessageDialog(this, "okokok");
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnThemActionPerformed

