    public void loadCBO() {
        cboLop.removeAllItems();
        String sql = "select * from dbo.Class";
        try {
            Connection conn = jdbcUtill.ketNoi();
            PreparedStatement pstmt = conn.prepareStatement(sql);

            ResultSet rs = pstmt.executeQuery();
            while (rs.next()) {
                cboLop.addItem(rs.getString(1));
            }
            conn.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

